function exportTableToCSV(filename,tableElementId) {
    var csv = [];
    var rows = document.getElementById(tableElementId).rows;
    for (var i = 0; i < rows.length; i++) {
        var row = [], cols = rows[i].querySelectorAll("td, th");
        for (var j = 0; j < cols.length; j++)
            row.push(cols[j].innerText);
        csv.push(row.join(","));
    }

    // Download CSV file
    downloadCSV(csv.join("\n"), filename);
}
function exportTableToExcel(filename,tableElementId) {
    var excel = [];
    var rows = document.getElementById(tableElementId).rows;
    for (var i = 0; i < rows.length; i++) {
        var row = [], cols = rows[i].querySelectorAll("td, th");
        for (var j = 0; j < cols.length; j++)
            row.push(cols[j].innerText);
        excel.push(row);
    }

    // Download CSV file
    downloadExcel(excel.join("\n"), filename);
}

function downloadExcel(excel, filename) {
    var excelFile;
    var downloadLink;

    // CSV file
    excelFile = new Blob([excel], {type: "text/excel"});

    // Download link
    downloadLink = document.createElement("a");

    // File name
    downloadLink.download = filename;

    // Create a link to the file
    downloadLink.href = window.URL.createObjectURL(excelFile);

    // Hide download link
    downloadLink.style.display = "none";

    // Add the link to DOM
    document.body.appendChild(downloadLink);

    // Click download link
    downloadLink.click();
}

